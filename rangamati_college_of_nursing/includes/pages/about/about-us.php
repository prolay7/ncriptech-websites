<h2>About <span>RANGAMATI SHIKSHAN ASHRAM</span></h2>
<p>	
RANGAMATI SHIKSHAN ASHRAM, B.Ed & D.El.Ed. embodies the spirit of collectivism and collaboration to create an institution of excellence. It was a dream dreamt by the visionaries of the students of West Bengal that have shared a common culture and a tradition of pursuit of excellence in learning. .</p>
<p>When the Institute thus came into existence, it epitomized the futuristic vision of the education that foresaw the need for raising the benchmark of quality in teaching that would, on the one hand, strengthen the competence of every learner across the curriculum and on the other, catalyse a process of quality improvement in all the aspects of school education. To realize this lofty and worthy dream, the learned advisories of the Institute came together and resolved to nurture, support and guide the growth of the Institute as a premier body of teacher training, research and empowerment.</p>
